// Fonctions utilitaires
export const getAllIcons = () => {
    return iconCategories.flatMap(category => category.icons)
}

export const searchIcons = (query, categories = iconCategories) => {
    if (!query) return getAllIcons()

    const searchTerm = query.toLowerCase()
    return getAllIcons().filter(icon =>
        icon.name.toLowerCase().includes(searchTerm) ||
        icon.label.toLowerCase().includes(searchTerm)
    )
}

export const getIconsByCategory = (categoryName) => {
    const category = iconCategories.find(cat => cat.name === categoryName)
    return category ? category.icons : []
}

export const getTotalIconCount = () => {
    return getAllIcons().length
}

export const getCategoryCount = () => {
    return iconCategories.length
} 
